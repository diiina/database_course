SELECT pay.dt,concat(LPAD(round(( PU/DAU * 100 ),2),5,'0'),' %') PPU
FROM (
    SELECT DATE(payment_dttm) dt, COUNT(DISTINCT user_id) PU
    FROM payments
    GROUP BY dt )pay
INNER JOIN (
    SELECT DATE(begin_dttm) dt, COUNT(DISTINCT user_id) DAU
    FROM sessions
    GROUP BY dt)sess ON sess.dt = pay.dt;

